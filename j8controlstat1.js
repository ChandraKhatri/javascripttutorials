// switch statement
// The switch statement is used to perform different actions based on different conditions.
// Use the switch statement to select one of many code blocks to be executed.
let month = 2;
switch(month){
    case 1:
    console.log("january");
    break;
    case 2:
    console.log("February");
    break;
    case 3:
    console.log("March");
    // case 4:
    // console.log("February");
    break;
    default:
        console.log("error");
        break;
}